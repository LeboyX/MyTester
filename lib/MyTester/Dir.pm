#!perl
package MyTester::Dir;
use Moose;
use MooseX::Method::Signatures;
use MooseX::StrictConstructor;

use Carp;

################################################################################
# Imports
################################################################################

################################################################################
# Attributes
################################################################################

has 'name' => (
   isa => 'Str',
   is => 'rw',
   default => ".",
   trigger => sub {
      my ($self, $val) = @_;
      croak "'$val' is not a valid directory" if !-d $val;
   }
); 

