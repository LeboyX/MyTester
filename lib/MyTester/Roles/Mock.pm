=head1 Name

MyTester::Roles::Mock - Top-level class describing the purpose of mock'd objects

=head1 Version

No set version right now

=head1 Description

All classes under the MyTester::Roles::Mock namespace are basic, simplistic 
implementations of the various roles under MyTester::Roles. they are used 
primarily for testing purposes to exercise role functionality w/o incurring
the overhead of continually writing simple consumers of each role. The roles
contained w/in this namespace are not explicitly tested, as their intended
purpose was to aid testing.  

=cut